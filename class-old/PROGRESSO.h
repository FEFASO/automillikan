#ifndef PROGRESSO_H_
#define PROGRESSO_H_

#include <iostream>
#include <time.h>
using namespace std;

class PROGRESSO
{
   // //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
   // ATRIBUTOS DO OBJETO
   // //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
   private:
      time_t clocks_ini;               // Numero de clocks no inicio da contagem do progresso
      int progresso_total;             // Progresso total
      short dezena_anterior;           // Ultima dezena do progresso que foi mostrada
   // //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
   // CONSTRUTORES E DESTRUTORES
   // //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
   public:
      // Dado Inicia um progresso
      PROGRESSO(const char *mensagem, int progresso_total);
      // Finaliza um progresso e escreve na tela o tempo total de execucao
      virtual ~PROGRESSO();
   // //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
   // METODOS
   // //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
   public:
      // Dado o progresso atual, escreve na tela em porcentagem o quanto ja' foi feito do progresso
      void atualiza(int progresso_atual);
      // Finaliza um progresso
      void finaliza();
};

#endif /* PROGRESSO_H_ */
